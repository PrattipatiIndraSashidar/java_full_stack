public class  Unary  {
    public static void main(String[] args) {
        int a = 10, b = 5;

        System.out.println("\nUnary Operators:");
        int d = 10;
        System.out.println("d = " + d);
        System.out.println("++d = " + (++d));
        System.out.println("--d = " + (--d));
        System.out.println("-d = " + (-d));
    }
}
